    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        user u = new user();
        try {
            this.stat = k.getCon().prepareStatement("select * from user where " + "Username='" + u.Username + "'and Password='" + u.Password + "';");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                u.ID_level = rs.getInt("ID_level");
            }

            if (u.ID_level == 0) {
                JOptionPane.showMessageDialog(null, "AKU TIDAK DI TEMUKAN");
            } else {
                switch (u.ID_level) {
                    case 1:
                        registrasipetugas reg = new registrasipetugas();
                        reg.setVisible(true);
                        this.setVisible(false);
                        break;
                    case 2:
                        menu_utama me = new menu_utama();
                        me.setVisible(true);
                        this.setVisible(false);
                        break;
                    case 3:
                        penjualan pe = new penjualan();
                        pe.setVisible(true);
                        this.setVisible(false);
                        break;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "username or password Not Method", "login Error", 1);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

